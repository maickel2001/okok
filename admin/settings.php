<?php
// Lightweight boot without DB to avoid blank page if DB down
require_once __DIR__ . '/../config/database.php';
if (session_status() !== PHP_SESSION_ACTIVE) { session_start(); }

// Enable error display for this admin page only
if (function_exists('ini_set')) { @ini_set('display_errors', '1'); }
@error_reporting(E_ALL);

// Admin session check
if (!isset($_SESSION['admin_id'])) {
    $base = rtrim(SITE_URL, '/');
    header('Location: ' . $base . '/admin/login.php');
    exit();
}

$adminName = isset($_SESSION['admin_name']) ? $_SESSION['admin_name'] : 'Admin';

// Separate connection test (do not crash the page)
$dbStatus = ['ok' => false, 'message' => ''];
try {
    $pdo = new PDO(
        'mysql:host=' . DB_HOST . ';dbname=' . DB_NAME . ';charset=utf8mb4',
        DB_USER,
        DB_PASS,
        [
            PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
            PDO::ATTR_DEFAULT_FETCH_MODE => PDO::FETCH_ASSOC,
            PDO::ATTR_EMULATE_PREPARES => false,
        ]
    );
    // Quick ping
    $pdo->query('SELECT 1');
    $dbStatus['ok'] = true;
    $dbStatus['message'] = 'Connexion DB OK';
} catch (Throwable $e) {
    $dbStatus['ok'] = false;
    $dbStatus['message'] = 'Erreur DB: ' . $e->getMessage();
}

// Current values
$current = [
    'SITE_NAME' => SITE_NAME,
    'SITE_URL' => SITE_URL,
    'UPLOAD_DIR' => UPLOAD_DIR,
    'LOGS_DIR' => defined('LOGS_DIR') ? LOGS_DIR : 'logs/',
];

$success = '';
$error = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $siteName = trim(isset($_POST['SITE_NAME']) ? $_POST['SITE_NAME'] : '');
    $siteUrl = trim(isset($_POST['SITE_URL']) ? $_POST['SITE_URL'] : '');
    $uploadDir = trim(isset($_POST['UPLOAD_DIR']) ? $_POST['UPLOAD_DIR'] : '');
    $logsDir = trim(isset($_POST['LOGS_DIR']) ? $_POST['LOGS_DIR'] : '');

    if ($siteName === '' || $siteUrl === '' || $uploadDir === '' || $logsDir === '') {
        $error = "Tous les champs sont obligatoires.";
    } else {
        // Normalize directories with trailing slash
        if ($uploadDir !== '' && substr($uploadDir, -1) !== '/') { $uploadDir .= '/'; }
        if ($logsDir !== '' && substr($logsDir, -1) !== '/') { $logsDir .= '/'; }

        // Build local settings PHP
        $vars = [
            'SITE_NAME' => $siteName,
            'SITE_URL' => $siteUrl,
            'UPLOAD_DIR' => $uploadDir,
            'LOGS_DIR' => $logsDir,
        ];
        $php = "<?php\n// Generated by admin/settings on ".date('c')."\n";
        foreach ($vars as $k => $v) {
            $escaped = var_export($v, true);
            $php .= "putenv('{$k}=' . {$escaped});\n";
            $php .= "\n// Also sync into \\$_ENV/\\$_SERVER\n";
            $php .= "\\$_ENV['{$k}'] = {$escaped};\n";
            $php .= "\\$_SERVER['{$k}'] = {$escaped};\n";
        }

        $target = __DIR__ . '/../config/local_settings.php';
        $targetDir = dirname($target);
        if (!is_dir($targetDir)) { @mkdir($targetDir, 0775, true); }

        $bytes = @file_put_contents($target, $php);
        if ($bytes === false) {
            $error = "Impossible d'enregistrer les paramètres (permissions insuffisantes sur le dossier config/).";
        } else {
            $success = 'Paramètres mis à jour avec succès.';
            // Update in-memory values for immediate reflect
            $current['SITE_NAME'] = $siteName;
            $current['SITE_URL'] = $siteUrl;
            $current['UPLOAD_DIR'] = $uploadDir;
            $current['LOGS_DIR'] = $logsDir;
        }
    }
}

// Plain fallback mode (no CSS) to avoid any rendering issue
if (isset($_GET['plain']) && $_GET['plain'] == '1') {
    header('Content-Type: text/html; charset=UTF-8');
    echo '<!doctype html><html><head><meta charset="utf-8"><title>Paramètres (fallback)</title></head><body style="font-family: Arial; padding:20px;">';
    echo '<h1>Paramètres du site (mode simplifié)</h1>';
    echo '<p><strong>Status DB:</strong> '.htmlspecialchars($dbStatus['message']).'</p>';
    if (!empty($error)) { echo '<div style="color:#ff4444;">'.$error.'</div>'; }
    if (!empty($success)) { echo '<div style="color:#00aa66;">'.$success.'</div>'; }
    echo '<form method="POST">';
    echo '<div><label>Nom du site</label><br><input type="text" name="SITE_NAME" value="'.htmlspecialchars($current['SITE_NAME']).'" style="width:400px;"></div><br>';
    echo '<div><label>URL du site</label><br><input type="url" name="SITE_URL" value="'.htmlspecialchars($current['SITE_URL']).'" style="width:400px;"></div><br>';
    echo '<div><label>Dossier/URL uploads</label><br><input type="text" name="UPLOAD_DIR" value="'.htmlspecialchars($current['UPLOAD_DIR']).'" style="width:400px;"></div><br>';
    echo '<div><label>Dossier logs</label><br><input type="text" name="LOGS_DIR" value="'.htmlspecialchars($current['LOGS_DIR']).'" style="width:400px;"></div><br>';
    echo '<button type="submit">Enregistrer</button> ';
    echo '<a href="dashboard.php">Retour</a>';
    echo '</form></body></html>';
    exit;
}
