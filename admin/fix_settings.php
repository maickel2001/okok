    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>Fix Settings - Admin <?php echo htmlspecialchars(SITE_NAME); ?></title>
    <link rel="stylesheet" href="<?php echo rtrim(SITE_URL, '/'); ?>/assets/css/style.css">
    <link rel="stylesheet" href="https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.0.0/css/all.min.css">
</head>
<body>
    <nav class="navbar">
        <div class="container">
            <div class="nav-content">
                <a href="<?php echo rtrim(SITE_URL, '/'); ?>/admin/dashboard.php" class="logo">
                    <i class="fas fa-wrench"></i> Fix Settings
                </a>
                <ul class="nav-links">
                    <li><a href="<?php echo rtrim(SITE_URL, '/'); ?>/admin/diagnostics.php">Diagnostics</a></li>
                    <li><a href="<?php echo rtrim(SITE_URL, '/'); ?>/logout.php" class="btn btn-secondary">Déconnexion</a></li>
                </ul>
            </div>
        </div>
    </nav>

    <div class="dashboard">
        <div class="container">
            <div class="card" style="margin-bottom: 1.5rem;">
                <h2 style="color: var(--primary-color); margin-bottom: 1rem;"><i class="fas fa-sliders-h"></i> Paramètres proposés</h2>
                <div style="display:grid; gap:0.5rem; color: var(--text-secondary);">
                    <?php foreach ($defaults as $k => $v): ?>
                        <div><strong><?php echo htmlspecialchars($k); ?>:</strong> <span style="color: var(--text-primary);"><?php echo htmlspecialchars($v); ?></span></div>
                    <?php endforeach; ?>
                </div>
                <form method="POST" style="margin-top: 1rem;">
                    <button type="submit" class="btn btn-primary"><i class="fas fa-save"></i> Appliquer et tester</button>
                    <a href="<?php echo rtrim(SITE_URL, '/'); ?>/admin/settings.php" class="btn btn-secondary">Aller aux Settings</a>
                </form>
            </div>

            <?php if (!empty($messages)): ?>
            <div class="card">
                <h3 style="color: var(--primary-color); margin-bottom: 1rem;"><i class="fas fa-list"></i> Résultats</h3>
                <div style="display:grid; gap:0.5rem;">
                    <?php foreach ($messages as $m): ?>
                        <div style="padding:0.75rem; border:1px solid var(--border-color); border-radius:8px; background: var(--secondary-color);">
                            <?php echo htmlspecialchars($m); ?>
                        </div>
                    <?php endforeach; ?>
                </div>
            </div>
            <?php endif; ?>
        </div>
    </div>
</body>
</html>